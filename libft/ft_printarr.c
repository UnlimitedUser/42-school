#include "libft.h"

void	ft_printarr(int arr[], int n)
{
	int	a;

	a = -1;
	ft_putchar('[');
	while (++a < n)
	{
		ft_putnbr(arr[a]);
		if (a + 1 != n)
			ft_putstr(", ");
	}
	ft_putchar(']');
}
