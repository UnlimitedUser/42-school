#include "fillit.h"

void	ft_memdela(void ***mem)
{
	if (mem && *mem && **mem)
	{
		while (**mem)
		{
			free(**mem);
			**mem = NULL;
		}
		free(*mem);
		*mem = NULL;
	}
}
